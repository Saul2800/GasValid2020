    private void eliminarELCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarELCActionPerformed

        String id_Crono,reid_Crono;

        LibreriaBDControlador lbd= new LibreriaBDControlador();
        lbd.openConnection();

        id_Crono=idELC.getText();
        reid_Crono=reingresarIdELC.getText();

        if(id_Crono.equals(reid_Crono)){

            lbd.eliminarCronometro(id_Crono);
        }
        else{
            JOptionPane.showMessageDialog(null, "LOS DATOS de las entradas NO COINCIDEN por favor revise");
        }

        lbd.closeConnection();
    }//GEN-LAST:event_eliminarELCActionPerformed

